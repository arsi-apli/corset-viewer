package sk.arsi.corset.model;

public final class Pt {

    private final double x;
    private final double y;

    public Pt(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
